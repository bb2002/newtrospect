// esbuild 로 injection/entry.ts 를 IIFE 로 번들하여 src/injection-bundle.gen.ts 로 출력.
// 이 번들은 WebView 의 injectedJavaScript prop 에 그대로 주입된다.
//
// 왜 빌드 타임에 번들하는가:
//   - Metro/Hermes 환경은 DOM API(Readability·document.createTreeWalker) 가 없어
//     core/extract.ts 같은 모듈을 RN 본체로 직접 import 하면 런타임 에러.
//   - 번들을 *문자열 상수* 로 만들어 WebView 안(=실제 DOM 가진 Chromium)에서만 실행하면
//     RN 환경과 격리 + 코드 공유 둘 다 가능.
import { build } from "esbuild";
import { writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");

const result = await build({
  absWorkingDir: projectRoot,
  entryPoints: [path.resolve(projectRoot, "injection/entry.ts")],
  bundle: true,
  platform: "browser",
  format: "iife",
  target: ["chrome90"],
  minify: true,
  write: false,
  resolveExtensions: [".ts", ".tsx", ".js", ".mjs", ".cjs"],
  loader: { ".ts": "ts" },
  define: { "process.env.NODE_ENV": '"production"' },
  logLevel: "warning",
  // file: 의존성(@newtrospect/core)이 symlink 로 들어올 때 esbuild 가
  // realpath 를 따라가 packages/core 위치 기준으로 readability 등을 찾으면
  // 실패한다 (EAS cloud 의 npm ci 환경에서 재현). symlink 경로를 그대로 유지하면
  // mobile/node_modules 에 hoisted 된 deps 를 잘 찾음.
  preserveSymlinks: true,
  // 백업: 어떤 위치에서든 mobile/node_modules 를 모듈 검색 경로에 추가
  nodePaths: [path.resolve(projectRoot, "node_modules")],
});

const code = result.outputFiles[0].text;
const outPath = path.resolve(projectRoot, "src/injection-bundle.gen.ts");
const outDir = path.dirname(outPath);
await mkdir(outDir, { recursive: true });

// JSON.stringify 로 escape — 백틱·달러표시·개행 모두 안전.
// 끝에 `\n;true;` 붙임 — react-native-webview 가 Android 에서 evaluate 결과를
// 직렬화하지 못해 크래시하는 케이스 회피 (공식 권장 패턴).
const out = `// AUTO-GENERATED by scripts/build-injection.mjs. Do not edit.
// Source: injection/entry.ts → esbuild IIFE.
export const INJECTION_BUNDLE = ${JSON.stringify(code + "\n;true;")};
export const INJECTION_BUNDLE_BYTES = ${code.length};
`;

await writeFile(outPath, out, "utf8");
console.log(`[injection] ${code.length} bytes → src/injection-bundle.gen.ts`);
